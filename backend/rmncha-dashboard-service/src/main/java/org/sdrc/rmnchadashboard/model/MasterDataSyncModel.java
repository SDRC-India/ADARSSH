package org.sdrc.rmnchadashboard.model;

public class MasterDataSyncModel {
	
	private String areaSyncDate;
	private String areaLevelSyncDate;
	private String unitSyncDate;
	private String indicatorSyncDate;
	private String subgroupSyncDate;
	private String sectorSyncDate;
	private String sourceSyncDate;
	
	public String getAreaSyncDate() {
		return areaSyncDate;
	}
	public void setAreaSyncDate(String areaSyncDate) {
		this.areaSyncDate = areaSyncDate;
	}
	public String getAreaLevelSyncDate() {
		return areaLevelSyncDate;
	}
	public void setAreaLevelSyncDate(String areaLevelSyncDate) {
		this.areaLevelSyncDate = areaLevelSyncDate;
	}
	public String getUnitSyncDate() {
		return unitSyncDate;
	}
	public void setUnitSyncDate(String unitSyncDate) {
		this.unitSyncDate = unitSyncDate;
	}
	public String getIndicatorSyncDate() {
		return indicatorSyncDate;
	}
	public void setIndicatorSyncDate(String indicatorSyncDate) {
		this.indicatorSyncDate = indicatorSyncDate;
	}
	public String getSubgroupSyncDate() {
		return subgroupSyncDate;
	}
	public void setSubgroupSyncDate(String subgroupSyncDate) {
		this.subgroupSyncDate = subgroupSyncDate;
	}
	public String getSectorSyncDate() {
		return sectorSyncDate;
	}
	public void setSectorSyncDate(String sectorSyncDate) {
		this.sectorSyncDate = sectorSyncDate;
	}
	public String getSourceSyncDate() {
		return sourceSyncDate;
	}
	public void setSourceSyncDate(String sourceSyncDate) {
		this.sourceSyncDate = sourceSyncDate;
	}
	
	
}
